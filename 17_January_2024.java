/******************************************************************************
1. What is the output of the following code?
*******************************************************************************/

public class Solution {
    public static void main(String args[]) {
        int a = 7, b = 18, c = 45;
        a -= 4;
        b *= 2;
        c /= 9;
        System.out.println(a + "" + b + "" + c);
    }
}

/******************************************************************************
Output:- 3 36 5
*******************************************************************************/