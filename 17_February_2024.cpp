/******************************************************************************
1. What is the output of the following code?
*******************************************************************************/

#include <iostream>
using namespace std;

int main() {
    int a[] = {5,1,15,20,25};
    int i, j, k;
    i = ++a[1];
    j = a[1]++;
    k = a[i++];

    cout << i << " " << j << " " << k;
    return 0;
}

/******************************************************************************
Output:- 3 2 15
*******************************************************************************/