/******************************************************************************
1. What will be the output of the following code?
*******************************************************************************/

public class Solution {
    public static void main(String args[]) {
        for (int i = 0; i < 5; i++) {
            if (i == 3) {
                break;
            }
            System.out.print(i + "");
        }
    }
}

/******************************************************************************
Output:- 0 1 2
*******************************************************************************/
