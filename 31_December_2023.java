/******************************************************************************
1. What will be the output of the following code? 
*******************************************************************************/

public class Solution {
    public static void main(String args[]) {
        int a = 1;
        while (a < 4) {
            System.out.print(a + " ");
            a++;
        }
    }
}

/******************************************************************************
Output:- 1 2 3
*******************************************************************************/

