/******************************************************************************
1. What will be the output of the following code?
*******************************************************************************/

public class Solution {
    public static void main(String[] args) {
        do {
            System.out.print(1 + " ");
            do {
                System.out.print(2);
            } while (false);
        } while (false);
    }
}

/******************************************************************************
Output:- 1 2
*******************************************************************************/

